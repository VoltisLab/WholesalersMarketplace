  static CountryCode getByCode(String code) {
    return countries.firstWhere(
      (country) => country.code == code,
      orElse: () => countries.first, // Default to US
    );
  }

  static CountryCode getByDialCode(String dialCode) {
    return countries.firstWhere(
      (country) => country.dialCode == dialCode,
      orElse: () => countries.first, // Default to US
    );
  }

  static List<CountryCode> search(String query) {
    if (query.isEmpty) return countries;
    
    final lowerQuery = query.toLowerCase();
    return countries.where((country) =>
      country.name.toLowerCase().contains(lowerQuery) ||
      country.dialCode.contains(query) ||
      country.code.toLowerCase().contains(lowerQuery)
    ).toList();
  }
